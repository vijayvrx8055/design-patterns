package com.vrx.designpatterns;

import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

@SpringBootApplication
public class DesignPatternsApplication {

	public static void main(String[] args) {
		SpringApplication.run(DesignPatternsApplication.class, args);
	}
	/*
	 * Design Patterns
	 * ---------------------------
	 * Design patterns are well proved solution of commonly occuring problems in
	 * software design.
	 * Design patterns represent an idea.
	 *
	 * Categorization of Design Patterns
	 * --------------------------------
	 * 1. Creational Design Patterns : provides approach to create objects
	 * ==> Factory pattern, Builder pattern, Singleton Pattern etc.
	 *
	 * 2. Structural Design Patterns : provides approach to structure the code
	 * ==> Proxy pattern, Adaptor pattern etc.
	 *
	 * 3. Behavioral Design Patterns : provides approach regarding interaction
	 * between the objects and implementing loose coupling
	 * ==> Observer pattern, State pattern, Iterator pattern etc.
	 *
	 * 
	 * -----------------------------------------------
	 * The Gang of Four - "Design Patterns, element of reusable object-oriented
	 * software" book was written by a group of four persons named as
	 * Erich Gamma, Richard Helm,Ralph Johnson and John Vlissides in 1995.
	 */

}
